

class Islemler:
    def __init__(self,num1,num2):
        self.num1=num1
        self.num2=num2
    def topla(self):
        return self.num1+self.num2
    def cikar(self):
        return self.num1-self.num2
    def carp(self):
        return self.num1*self.num2
    def bol(self):
        return self.num1/self.num2

